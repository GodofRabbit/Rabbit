package com.shop.entity;
public class SearchCar {
	private String[] type_conditions;
	private String[] price_conditions;
	private String[] title_conditions;
	public String[] getType_conditions() {
		return type_conditions;
	}
	public void setType_conditions(String[] type_conditions) {
		this.type_conditions = type_conditions;
	}
	public String[] getPrice_conditions() {
		return price_conditions;
	}
	public void setPrice_conditions(String[] price_conditions) {
		this.price_conditions = price_conditions;
	}
	public String[] getTitle_conditions() {
		return title_conditions;
	}
	public void setTitle_conditions(String[] title_conditions) {
		this.title_conditions = title_conditions;
	}
	public SearchCar(String[] type_conditions, String[] price_conditions,
			String[] title_conditions) {
		super();
		this.type_conditions = type_conditions;
		this.price_conditions = price_conditions;
		this.title_conditions = title_conditions;
	}
	public SearchCar() {
		super();
	}
	
	
	
	

}
